trait Foo {
  const FOO: u32 = 0;
}

struct Bar {}

impl Bar {
  const BAR: u32 = 50;
}

impl Foo for Bar {}

enum Baz {}

impl Baz {
  const BAZ: u32 = 100;
}

impl Foo for Baz {}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn it_works() {
        // assert_eq!(Foo::FOO, 0);
        assert_eq!(Bar::BAR, 50);
        assert_eq!(Bar::FOO, 0);
        assert_eq!(Baz::BAZ, 100);
        assert_eq!(Baz::FOO, 0); 
    }
}
